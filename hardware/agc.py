"""AGC 自动增益控制缺陷模型 — CIR 观测层近似。

═══════════════════════════════════════════════════════════════════════════
误差层级: 芯片级 (Chip-level) — 接收机模拟前端
═══════════════════════════════════════════════════════════════════════════
AGC 在真实接收机里位于模拟前端/ADC 之前。它调节放大器增益，
使信号幅度落在 ADC 的最佳输入范围内（既不过小以致浪费动态范围，也不
过大以致饱和削顶）。

本项目关注定位层测距误差，不做完整模拟前端链路。因此当前实现不处理
连续时域波形、VGA 环路或协议前导码锁定过程，而是把 AGC 的主要后果
近似为 CIR 包络上的离散增益步进与可选削顶。

═══════════════════════════════════════════════════════════════════════════
物理原理
═══════════════════════════════════════════════════════════════════════════
AGC 工作于一个反馈环路:

  输入信号 → VGA(可变增益放大器) → ADC → 功率检测
                ↑                        │
                └────── 增益控制逻辑 ←────┘

三个非理想效应:

  (1) 离散增益步进 (Gain Step Granularity):
      AGC 的增益不是连续可调的，而是以固定步长（通常 3 dB/档）
      在有限个档位之间切换。

      增益档位: g ∈ {g_min, g_min + step, g_min + 2×step, ..., g_max}
      理想增益 g_ideal 被"四舍五入"到最近的档位 g_actual。

      后果: 信号幅度永远不能被调到"刚刚好"。
            增益过高 → 信号偏大 → 接近 ADC 满量程 → 强径可能削顶
            增益过低 → 信号偏小 → 弱径低于 ADC 最小分辨 → 丢失

  (2) 增益切换滞后 (Settling Time):
      AGC 检测功率变化后需要一定时间（若干微秒）才能完成增益调整。
      在此期间，信号被错误的增益放大。

      对 OFDM (WiFi/5G): 前导码只有几十微秒，AGC 必须在极短时间内锁定。
      如果锁定不及时，前导码的前半部分可能被错误增益放大。

      对 UWB: 脉冲极短（2ns），AGC 在整个测距帧期间基本不变 → 影响较小。

  (3) 饱和削顶 (Saturation / Clipping):
      当放大器输出超过供电电压时，信号被硬削顶:

              ╱╲                ┌──────          ┌──────
             ╱  ╲     →       │                │
            ╱    ╲            │      →         │
      ─────╱──────╲─────     ───────┘         ───────┘

      削顶使 CIR 包络的峰值被"切平"，统计特性改变:
      - 峰值不再是真正的信道峰值，而是放大器的饱和电压
      - ThresholdLde 用 30% 峰值做门限 → 因为峰值被压低 → 门限偏低
      - 门限偏低 → 噪声更容易误触发 → P90 误差增大

═══════════════════════════════════════════════════════════════════════════
对测距的影响机制（定位层近似）
═══════════════════════════════════════════════════════════════════════════

  场景 A — 增益过高导致削顶:
    弱信号 → AGC 切到最高增益档 → 突然来一个强反射径 → 放大器饱和
    → CIR 峰值被削顶 → 峰值比例门限算法（ThresholdLde）的参照值失真
    → 门限设错 → 可能漏掉首径或误触噪声

  场景 B — 增益过低:
    强 LOS 信号 → AGC 切到最低增益档 → 后续弱多径、弱首径被压到噪声以下
    → CIR 看起来只有一条径 → 首径检测 = 峰值检测 → 对 LOS 影响不大

  场景 C — 增益切换恰好在测距帧中间:
    前导码前半用旧增益，后半用新增益 → 积累出的 CIR 是两种增益的混合
    → CIR 形状失真 → 首径检测点偏移

═══════════════════════════════════════════════════════════════════════════
适用的协议 / 传感器
═══════════════════════════════════════════════════════════════════════════
  所有接收机都有 AGC，但受影响程度不同:
  - WiFi (OFDM, 高 PAPR):   PAPR ~10dB → 强子载波可能削顶 → 影响较大
  - 5G NR (OFDM):           同 WiFi，但带宽更小 → PAPR 类似
  - UWB (脉冲):             占空比极低 → 峰值功率远高于平均功率 → 削顶风险
                            最大，但 DW1000 的 AGC 专为脉冲优化 → 风险可控

═══════════════════════════════════════════════════════════════════════════
配置参数
═══════════════════════════════════════════════════════════════════════════
  impairments:
    enable_agc: false            # 是否启用 AGC 效应
    agc_gain_step_db: 3.0        # 增益档位步长 (dB)，越小越精细
    agc_max_gain_db: 30.0        # 最大增益 (dB)
    agc_settling_us: 2.0         # 增益切换建立时间 (微秒)
    agc_clip_enable: false       # 是否启用饱和削顶
"""

from __future__ import annotations

import numpy as np


def apply_agc(
    cir_discrete: np.ndarray,
    gain_step_db: float = 3.0,
    max_gain_db: float = 30.0,
    settling_us: float = 2.0,
    clip_enable: bool = False,
    rng: np.random.Generator | None = None,
) -> np.ndarray:
    """模拟 AGC 对 CIR 包络的影响。

    当前实现是定位层近似: 离散增益步进 + 可选削顶。它直接作用在
    observed CIR envelope 上，不模拟模拟前端环路、I/Q 波形或前导码期间
    的真实 AGC 收敛过程。这样做是为了让测距算法看到合理的动态范围/削顶
    后果，同时避免把项目变成完整接收机仿真器。

    Parameters
    ----------
    cir_discrete : ndarray
        归一化的离散 CIR 包络。
    gain_step_db : float
        AGC 增益档位步长 (dB)。典型值 3 dB。
    max_gain_db : float
        AGC 最大增益 (dB)。超过此值不再放大。
    settling_us : float
        增益切换建立时间 (微秒)。当前保留，未激活。
    clip_enable : bool
        是否模拟削顶。开启后，幅度超过阈值 CIR 样本被截断。
    rng : Generator or None
        随机数生成器（当前保留，用于未来的随机增益抖动）。

    Returns
    -------
    cir_discrete : ndarray
        AGC 处理后的 CIR 包络。
    gain_linear : float
        实际应用的线性增益系数（用于同步放大连续 CIR）。
    """
    _ = settling_us   # 保留，用于未来模拟增益切换滞后

    if cir_discrete.size == 0:
        return cir_discrete, 1.0

    # (1) 离散增益步进
    #     AGC 只能以 gain_step_db 为步长切换增益。
    #     需要放大量 = 1.0 / peak（把峰值调到满量程）
    #     但这个放大量被量化为最近的增益档位 → 实际放大不是"刚刚好"。
    peak = float(np.max(cir_discrete))
    if peak <= 1e-12:
        return cir_discrete, 1.0

    ideal_gain_linear = 1.0 / peak
    ideal_gain_db = 20.0 * np.log10(max(ideal_gain_linear, 1e-12))

    # 增益量化为 gain_step_db 的整数倍
    gain_db = min(
        np.round(ideal_gain_db / gain_step_db) * gain_step_db,
        max_gain_db,
    )
    gain_linear = 10.0 ** (gain_db / 20.0)

    out = cir_discrete * gain_linear

    # (2) 可选的饱和削顶
    #     如果放大后超过 ADC 满量程（归一化 = 1.0），信号被硬削顶。
    if clip_enable:
        out = np.clip(out, 0.0, 1.0)

    return out, gain_linear
