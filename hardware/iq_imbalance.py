"""I/Q 不平衡缺陷模型 — 正交下变频混频器失配。

═══════════════════════════════════════════════════════════════════════════
误差层级: 芯片级 (Chip-level) — 接收机 RF 模拟前端
═══════════════════════════════════════════════════════════════════════════
所有零中频/低中频接收机用正交混频器把 RF 信号下变频为 I/Q 基带信号。
I 路和 Q 路是两条独立的模拟通路——两只混频管、两路放大器、两只 ADC。
硅片制造偏差导致两条通路不可能完美匹配。

═══════════════════════════════════════════════════════════════════════════
物理原理
═══════════════════════════════════════════════════════════════════════════

  理想下变频器:
    天线 → LNA → ┌─ I路: cos(2π·f_c·t) ──→ ADC ──→ I[n]
                │
           LO → ┤  ← 精确 90° 移相
                │
                └─ Q路: -sin(2π·f_c·t) ──→ ADC ──→ Q[n]

    r(t) = I(t) + j·Q(t)   ←  完美的复基带信号

  真实下变频器:
    天线 → LNA → ┌─ I路: (1+ε)cos(2π·f_c·t) ──→ ADC ──→ I[n]
                │
           LO → ┤  ← 90° + φ (相位偏差 φ 度)
                │
                └─ Q路: -(1-ε)sin(2π·f_c·t + φ) ──→ ADC ──→ Q[n]

    r'(t) = α·r(t) + β·r*(t)    ←  出现了共轭镜像项！

    α = cos(φ/2) + j·ε·sin(φ/2)   (≈ 1 + j·0     当 ε, φ 很小时)
    β = ε·cos(φ/2) - j·sin(φ/2)   (≈ ε - j·φ/2   当 ε, φ 很小时)

  频域:
    H'(f) = α·H(f) + β·H*(-f)

  关键: β 项把负频率的频谱搬到了正频率 → 镜像干扰。

═══════════════════════════════════════════════════════════════════════════
对测距的影响机制
═══════════════════════════════════════════════════════════════════════════

  频域: H'(f_k) = α·H(f_k) + β·H*(-f_k)
        每个子载波被它的"负频率对应子载波"污染。

  IFFT 后 → CIR:
    原始 CIR 的每条径 a_i·δ(τ-τ_i) 产生一个共轭镜像径 β/α · a*_i·δ(τ-τ_i)

  后果:
    1. 镜像径幅度 = |β/α| × 原始径幅度
       ε=0.03, φ=3° → |β/α| ≈ 0.04  →  强径的镜像径约 4%
    2. 镜像径位置和真径重叠（τ 相同），但复数增益不同
       → 在 CIR 包络层面，叠加后各径幅度发生变化
    3. 首径若本身就是弱径（NLOS 下可能 < 峰值的 5%），
       I/Q 不平衡造成的幅度变化可能使首径偏离检测门限

  对 UWB (DW1000/DW3000):
    - 零中频架构，无内置数字补偿
    - 镜像抑制比 (IRR) ≈ 25-35 dB → 镜像径幅度为峰值的 2~6%
    - 首径通常弱于峰值 6-15 dB → 镜像可能干扰

  对 WiFi/5G:
    - 通常有数字域 I/Q 补偿 → 残留 IRR > 40 dB
    - 影响较小，但未补偿的廉价芯片仍有明显 I/Q 不平衡

═══════════════════════════════════════════════════════════════════════════
本实现的模型
═══════════════════════════════════════════════════════════════════════════

  频域施加 (在 h_observed 上, IFFT 前):

    H'(f_k) = α·H(f_k) + β·H*(-f_k)

  其中频率镜像映射使用 numpy.fft.fftfreq 索引:
    f_k 的镜像索引 = (N - k) % N

  对 DC (k=0) 和 Nyquist (k=N/2, N 偶数): f_k = -f_k → 退化为实数缩放。

═══════════════════════════════════════════════════════════════════════════
适用的协议 / 传感器
═══════════════════════════════════════════════════════════════════════════
  所有使用正交下变频的接收机均受影响:

  - UWB (DW1000/DW3000):
      零中频架构, 130nm/40nm 工艺, 内置补偿有限
      IRR ≈ 25-35 dB → 受影响最明显

  - WiFi (802.11 a/g/n/ac/ax):
      通常有基带 I/Q 补偿算法, 残留 IRR > 40 dB
      影响较小, 但廉价网卡仍可能 IRR < 30 dB

  - 5G NR (FR1/FR2):
      基站级校准 + 数字补偿, 残留 IRR > 45 dB
      影响最小

═══════════════════════════════════════════════════════════════════════════
配置参数 (per-radio)
═══════════════════════════════════════════════════════════════════════════
  radio_cfg:
    enable_iq_imbalance: false        # 是否启用 I/Q 不平衡
    iq_gain_imbalance: 0.03           # 增益不平衡 ε (0.01~0.05)
    iq_phase_imbalance_deg: 3.0       # 相位不平衡 φ (1~5°)
"""

from __future__ import annotations

import numpy as np


def apply_iq_imbalance(
    h_frequency: np.ndarray,
    epsilon: float = 0.03,
    phi_deg: float = 3.0,
) -> np.ndarray:
    """在频域施加 I/Q 不平衡。

    H'(f_k) = α·H(f_k) + β·H*(-f_k)

    此操作在复数 CSI 上进行, IFFT 之前调用。频率镜像映射
    基于 numpy.fft.fftfreq 的索引规则: k → (N - k) % N。

    Parameters
    ----------
    h_frequency : ndarray, complex
        频域信道响应, fftfreq 顺序 (DC 在 index 0)。
    epsilon : float
        I/Q 增益不平衡。0.01=1% 增益差, 0.05=5% 增益差。
    phi_deg : float
        I/Q 相位不平衡 [度]。1°~5° 为典型值。

    Returns
    -------
    h_iq : ndarray, complex
        I/Q 不平衡后的频域响应。
    """
    if epsilon == 0.0 and phi_deg == 0.0:
        return h_frequency

    phi_rad = np.deg2rad(phi_deg)
    alpha = np.cos(phi_rad / 2.0) + 1j * epsilon * np.sin(phi_rad / 2.0)
    beta = epsilon * np.cos(phi_rad / 2.0) - 1j * np.sin(phi_rad / 2.0)

    if beta == 0.0:
        return alpha * h_frequency

    n = len(h_frequency)
    # 频率镜像索引: f_k → -f_k, 即 k → (N - k) % N
    mirror = np.empty(n, dtype=int)
    mirror[0] = 0
    for k in range(1, n):
        mirror[k] = n - k

    h_mirror = np.conj(h_frequency[mirror])
    return alpha * h_frequency + beta * h_mirror
